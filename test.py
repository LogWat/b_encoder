# 与えられたバイト列から，目的のASCII範囲への変換を行うための
# 変換式を求めるプログラム

for i in range(0x1, 0xFF + 1):
    # convert 0x1 ~ 0xFF to ASCII(0x61 ~ 0x70)
    b0 = i % 0x10   # 下位4bit
    b1 = i // 0x10  # 上位4bit
    b0 += 0x61      # 0x1 ~ 0xF -> 0x61 ~ 0x70
    b1 += 0x61      # 0x1 ~ 0xF -> 0x61 ~ 0x70
    print("{:X} => {}{}".format(i, chr(b1), chr(b0)))